package cn.edu.zucc.booklib.model;

public class BeanCut {
	private int cutno;
	private float cutprice;
	private boolean allow;
	private float cutnum;
	public int getCutno() {
		return cutno;
	}
	public void setCutno(int cutno) {
		this.cutno = cutno;
	}
	public float getCutprice() {
		return cutprice;
	}
	public void setCutprice(float cutprice) {
		this.cutprice = cutprice;
	}
	public boolean isAllow() {
		return allow;
	}
	public void setAllow(boolean allow) {
		this.allow = allow;
	}
	public float getCutnum() {
		return cutnum;
	}
	public void setCutnum(float cutnum) {
		this.cutnum = cutnum;
	}
	
}

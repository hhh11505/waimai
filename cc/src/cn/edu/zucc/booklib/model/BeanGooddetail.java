package cn.edu.zucc.booklib.model;

public class BeanGooddetail {
	private int gno;
	private int tno;
	private String gname;
	private float dno;
	private float youhuiprice;
	public int getGno() {
		return gno;
	}
	public void setGno(int gno) {
		this.gno = gno;
	}
	public int getTno() {
		return tno;
	}
	public void setTno(int tno) {
		this.tno = tno;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public float getDno() {
		return dno;
	}
	public void setDno(float dno) {
		this.dno = dno;
	}
	public float getYouhuiprice() {
		return youhuiprice;
	}
	public void setYouhuiprice(float youhuiprice) {
		this.youhuiprice = youhuiprice;
	}
}

package cn.edu.zucc.booklib.model;

public class BeanOrderdetail {
	private int ordno;
	private int gno;
	private int cnt;
	private float price;
	private float ayouhui;
	public int getOrdno() {
		return ordno;
	}
	public void setOrdno(int ordno) {
		this.ordno = ordno;
	}
	public int getGno() {
		return gno;
	}
	public void setGno(int gno) {
		this.gno = gno;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public float getAyouhui() {
		return ayouhui;
	}
	public void setAyouhui(float ayouhui) {
		this.ayouhui = ayouhui;
	}
	
}
